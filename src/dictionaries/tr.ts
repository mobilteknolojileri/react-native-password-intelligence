/**
 * Turkish password intelligence dictionaries for zxcvbn-ts.
 *
 * Strategy:
 * - keep one canonical Turkish source list per category
 * - generate ASCII and compact variants automatically
 * - avoid storing mojibake / duplicated manual variants
 *
 * Source categories are based on:
 * - TUIK common names / surnames / newborn names
 * - TFF visible 2025-2026 club landscape plus historically popular clubs
 * - Turkish province names and common plate-code password patterns
 * - common Turkish endearments, cultural keywords, and auth terms
 */

import type { TurkishDictionary } from '../types';

const COMPACT_PATTERN = /[\s'"’.-]+/g;

const ASCII_TR_MAP: Record<string, string> = {
  ç: 'c',
  ğ: 'g',
  ı: 'i',
  ö: 'o',
  ş: 's',
  ü: 'u',
};

const normalize = (value: string): string =>
  value.trim().toLocaleLowerCase('tr-TR');

const toAsciiTr = (value: string): string =>
  Array.from(value, (char) => ASCII_TR_MAP[char] ?? char).join('');

const compact = (value: string): string => value.replace(COMPACT_PATTERN, '');

const buildDictionary = (...groups: readonly string[][]): string[] => {
  const output: string[] = [];
  const seen = new Set<string>();

  for (const group of groups) {
    for (const rawValue of group) {
      const base = normalize(rawValue);
      if (!base) continue;

      const candidates = [
        base,
        compact(base),
        toAsciiTr(base),
        compact(toAsciiTr(base)),
      ];

      for (const candidate of candidates) {
        if (!candidate || seen.has(candidate)) continue;
        seen.add(candidate);
        output.push(candidate);
      }
    }
  }

  return output;
};

